import 'package:flutter/foundation.dart';

/// Counter state using ChangeNotifier
/// Demonstrates basic state management with Provider
class CounterState with ChangeNotifier {
  int _count = 0;

  int get count => _count;

  void increment() {
    _count++;
    notifyListeners();
  }

  void decrement() {
    _count--;
    notifyListeners();
  }

  void reset() {
    _count = 0;
    notifyListeners();
  }
}
